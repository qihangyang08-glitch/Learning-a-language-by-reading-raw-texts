// Build a lookup map from the Chinese gloss data (keyed by kanji word)
const zhMap = new Map();
// Also build by reading as fallback
const zhByReading = new Map();
for (const [w, r, p, g] of ZH_GLOSSES) {
  zhMap.set(w, { r, p, g });
  if (!zhByReading.has(r)) zhByReading.set(r, { w, r, p, g });
}

// Merge: replace glosses where we have Chinese, keep originals otherwise
let replaced = 0;
const output = ORIGINAL.map(entry => {
  const key = entry.w;
  if (zhMap.has(key)) {
    replaced++;
    const zh = zhMap.get(key);
    return { w: key, r: zh.r, p: zh.p || entry.p || '', g: zh.g };
  }
  // Try matching by reading for kana-only entries
  if (/^[ぁ-ん]+$/.test(key) && zhByReading.has(entry.r)) {
    replaced++;
    const zh = zhByReading.get(entry.r);
    return { w: key, r: zh.r, p: zh.p || entry.p || '', g: zh.g };
  }
  // Keep original English gloss
  return entry;
});

// Add new entries that aren't in the original dict
for (const [w, r, p, g] of ZH_GLOSSES) {
  if (!ORIGINAL.find(e => e.w === w)) {
    output.push({ w, r, p, g });
  }
}

console.log(`Replaced ${replaced} entries with Chinese glosses`);
